def augment(texts, n=3):
    import random
    result = list(texts)
    for text in texts:
        words = text.split()
        if len(words) > 4:
            for _ in range(n):
                w = words.copy()
                i = random.randint(0, len(w)-2)
                w[i], w[i+1] = w[i+1], w[i]
                result.append(' '.join(w))
    return result

np.random.seed(42)
H = augment(human_texts, 3)
A = augment(ai_texts, 3)
X = H + A
y = [0]*len(H) + [1]*len(A)

print(f"Датасет: {len(X)} мәтін ({len(H)} адам + {len(A)} ЖИ)")

pipeline = Pipeline([
    ('features', FeatureUnion([
        ('word', TfidfVectorizer(analyzer='word', ngram_range=(1,3),
                                  min_df=1, max_features=8000, sublinear_tf=True)),
        ('char', TfidfVectorizer(analyzer='char_wb', ngram_range=(3,6),
                                  min_df=1, max_features=8000, sublinear_tf=True)),
    ])),
    ('clf', LogisticRegression(C=1.0, max_iter=2000, random_state=42,
                                class_weight='balanced', solver='lbfgs'))
])

cv = cross_val_score(pipeline, X, y, cv=5, scoring='accuracy')
print(f"CV дәлдігі: {cv.mean():.1%} ± {cv.std():.1%}")
pipeline.fit(X, y)

